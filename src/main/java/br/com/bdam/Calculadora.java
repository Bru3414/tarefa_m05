package br.com.bdam;

/**
 * Calculadora de inteiros com métodos de soma, subtração, multiplicação e divisão
 */
public class Calculadora {

    /**
     * Método soma
     * @param a Inteiro 1 para somar
     * @param b Inteiro 2 para somar
     * @return a + b
     */
    public int soma(int a, int b) {
        return a + b;
    }

    /**
     * Método subtrair
     * @param a Inteiro 1 para subtrair
     * @param b Inteiro 2 para subtrair
     * @return a - b
     */
    public int subtrair(int a, int b) {
        return a - b;
    }

    /**
     * Método multiplicar
     * @param a Inteiro 1 para multiplicar
     * @param b Inteiro 2 para multiplicar
     * @return a * b
     */
    public int multiplicar(int a, int b) {
        return a * b;
    }

    /**
     * Método dividir
     * @param a Inteiro 1 para dividir
     * @param b Inteiro 2 para dividir
     * @return a / b
     */
    public int dividir(int a, int b) {
        return a / b;
    }
}
